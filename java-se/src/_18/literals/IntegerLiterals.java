package _18.literals;

public class IntegerLiterals {

	public static void main(String[] args) {
		
		// integer literals
		
		// 1) decimal literals
		
		int size = 100;
		
		// 2) octal literals -> basinda 0 olmali
		
		int eight = 010; 
		
		// 3) hexadecimal literals -> 0x
		
		int hexa = 0xA;
		
		int hexa2 = 0XAb;
		
		// 4) binary literals -> 0b
		
		int binary = 0b11;
	}
	
	
	
}
