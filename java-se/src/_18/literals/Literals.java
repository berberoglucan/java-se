package _18.literals;

public class Literals {

	public static void main(String[] args) {
		
		// floating point literals -> varsayilan olarak double tipindedir.
		
		double d = 100.0; // 64 bit
		
		double d2 = 100.0d;
		
		float f = 100.0f; // 32 bit
		
		// boolean literals -> true ve ya false
		
		boolean b = false;
		
		// character literals -> char veri tipi 16 bit unsigned
		
		char letterA = 65;
		
		char letterA2 = 'A';
		
		
	}
	
}
